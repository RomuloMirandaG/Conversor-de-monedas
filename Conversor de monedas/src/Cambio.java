public class Cambio {
    private double quetzalCambio;
    private double pesoCambio;
    private double dollarCambio;

    public double getQuetzalCambio() {
        return quetzalCambio;
    }

    public void setQuetzalCambio(double quetzalCambio) {
        this.quetzalCambio = quetzalCambio;
    }

    public double getPesoCambio() {
        return pesoCambio;
    }

    public void setPesoCambio(double pesoCambio) {
        this.pesoCambio = pesoCambio;
    }

    public double getDollarCambio() {
        return dollarCambio;
    }

    public void setDollarCambio(double dollarCambio) {
        this.dollarCambio = dollarCambio;
    }
}
